// Helper — pick the right language string
export function t<T extends { en: string | ((...a: any[]) => string); ar: string | ((...a: any[]) => string) }>(
  entry: T,
  ar: boolean,
): T['en'] {
  return (ar ? entry.ar : entry.en) as T['en']
}
